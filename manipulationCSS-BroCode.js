// How to change CSS property of Element

const myTitle = document.getElementById("title");

myTitle.style.color = "rgb(90,90,90)";
myTitle.style.backgroundColor = "lightblue";
myTitle.style.fontFamily = "sans serif";
myTitle.style.textAlign = "center";
myTitle.style.border = "2px solid";
